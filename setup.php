$error = '';
$success = '';
$step = chatspace_configured() ? 'admin' : 'database';

function setup_admin_exists(): bool {
    if (!chatspace_configured()) return false;
    try {
        $pdo = db();
        return (bool)$pdo->query("SELECT id FROM users WHERE role = 'admin' LIMIT 1")->fetchColumn();
    } catch (Throwable) {
        return false;
    }
}

