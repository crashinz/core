    redirect_to('/login.php');
}

if ($_SERVER['REQUEST_METHOD'] === 'POST' && ($_POST['step'] ?? '') === 'database') {
    $driver = (string)($_POST['db_driver'] ?? 'sqlite');
    if (!in_array($driver, ['sqlite', 'mysql'], true)) {
        $error = 'Choose SQLite or MySQL/MariaDB.';
    } else {
        try {
            if ($driver === 'sqlite') {
                if (!extension_loaded('pdo_sqlite')) throw new RuntimeException('PDO SQLite is required for SQLite installs.');
                if (!is_writable(__DIR__ . '/includes') || !is_writable(__DIR__ . '/db')) throw new RuntimeException('includes/ and db/ must be writable.');
                write_setup_config(['driver' => 'sqlite']);
                require_once CHATSPACE_CONFIG;
                db();
                redirect_to('/setup.php');
            }

            if (!extension_loaded('pdo_mysql')) throw new RuntimeException('PDO MySQL is required for MySQL/MariaDB installs.');
            $host = trim((string)($_POST['db_host'] ?? '127.0.0.1'));
            $port = (int)($_POST['db_port'] ?? 3306);
            $name = preg_replace('/[^A-Za-z0-9_]/', '', trim((string)($_POST['db_name'] ?? 'chatspace_ce')));
            $dbUser = trim((string)($_POST['db_user'] ?? ''));
            $dbPass = (string)($_POST['db_pass'] ?? '');
            if ($host === '' || $name === '' || $dbUser === '') throw new RuntimeException('Database host, name, and user are required.');

            $server = new PDO("mysql:host={$host};port={$port};charset=utf8mb4", $dbUser, $dbPass, [PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION]);
            $server->exec("CREATE DATABASE IF NOT EXISTS `{$name}` CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci");
            $test = new PDO("mysql:host={$host};port={$port};dbname={$name};charset=utf8mb4", $dbUser, $dbPass, [PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION, PDO::ATTR_DEFAULT_FETCH_MODE => PDO::FETCH_ASSOC]);
            migrate($test);
            write_setup_config(['driver' => 'mysql', 'host' => $host, 'port' => $port, 'name' => $name, 'user' => $dbUser, 'pass' => $dbPass]);
            redirect_to('/setup.php');
        } catch (Throwable $e) {
            $error = $e->getMessage();
            $step = 'database';
        }
    }
}

if ($_SERVER['REQUEST_METHOD'] === 'POST' && ($_POST['step'] ?? '') === 'admin') {
    $name = trim((string)($_POST['display_name'] ?? ''));
    $email = trim((string)($_POST['email'] ?? ''));
    $password = (string)($_POST['password'] ?? '');
    $confirm = (string)($_POST['confirm_password'] ?? '');
    try {
        if (!chatspace_configured()) throw new RuntimeException('Database setup is not complete.');
        if ($name === '' || !filter_var($email, FILTER_VALIDATE_EMAIL)) throw new RuntimeException('Display name and a valid email are required.');
        if (strlen($password) < 8 || $password !== $confirm) throw new RuntimeException('Use matching passwords of at least 8 characters.');
        $avatar = setup_avatar_upload();
        $pdo = db();
        $stmt = $pdo->prepare('INSERT INTO users (email, password_hash, display_name, role, avatar_path) VALUES (?,?,?,?,?)');
        $stmt->execute([$email, password_hash($password, PASSWORD_DEFAULT), $name, 'admin', $avatar]);
        $_SESSION['user_id'] = (int)$pdo->lastInsertId();
        redirect_to('/setup.php?done=1');
    } catch (Throwable $e) {
        $error = $e->getMessage();
        $step = 'admin';
    }
}

