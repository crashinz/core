}

function write_setup_config(array $cfg): void {
    $lines = ["<?php", "// Auto-generated by ChatSpace CE setup. Edit carefully.", "const CHATSPACE_DB_DRIVER = " . var_export($cfg['driver'], true) . ";"];
    if ($cfg['driver'] === 'mysql') {
        $lines[] = "const CHATSPACE_DB_HOST = " . var_export($cfg['host'], true) . ";";
        $lines[] = "const CHATSPACE_DB_PORT = " . (int)$cfg['port'] . ";";
        $lines[] = "const CHATSPACE_DB_NAME = " . var_export($cfg['name'], true) . ";";
        $lines[] = "const CHATSPACE_DB_USER = " . var_export($cfg['user'], true) . ";";
        $lines[] = "const CHATSPACE_DB_PASS = " . var_export($cfg['pass'], true) . ";";
    } else {
        $lines[] = "const CHATSPACE_SQLITE_PATH = __DIR__ . '/../db/chatspace.sqlite';";
    }
    file_put_contents(CHATSPACE_CONFIG, implode("\n", $lines) . "\n");
}

function setup_avatar_upload(): string {
    if (empty($_FILES['avatar']['tmp_name']) || !is_uploaded_file($_FILES['avatar']['tmp_name'])) {
        throw new RuntimeException('Admin avatar is required.');
    }
    $finfo = new finfo(FILEINFO_MIME_TYPE);
    $mime = $finfo->file($_FILES['avatar']['tmp_name']) ?: '';
    $allowed = ['image/jpeg' => 'jpg', 'image/png' => 'png', 'image/gif' => 'gif', 'image/webp' => 'webp'];
    if (!isset($allowed[$mime]) || (int)$_FILES['avatar']['size'] > 5 * 1024 * 1024) {
        throw new RuntimeException('Use a JPEG, PNG, GIF, or WEBP avatar under 5 MB.');
    }
    $dir = __DIR__ . '/assets/uploads/avatars';
    if (!is_dir($dir)) mkdir($dir, 0775, true);
    $file = bin2hex(random_bytes(12)) . '.' . $allowed[$mime];
    $dest = $dir . '/' . $file;
    if (!move_uploaded_file($_FILES['avatar']['tmp_name'], $dest)) {
        throw new RuntimeException('Could not save admin avatar.');
    }
    return '/assets/uploads/avatars/' . $file;
}

